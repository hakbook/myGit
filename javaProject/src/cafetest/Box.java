package cafetest;

public class Box {
	public static void main(String[] args) {
		double width = 10.0;
		double column = 5.0;
		
		double area = width * column;
		double box = 2.0 * (width + column);
		
		
		System.out.println("사각형의 넓이 : " + area);
		System.out.println("사각형의 둘레 : " +  box);
		
		
	}

}
